
def convertir_a_lista_9x9(cadena):
    lista_9x9 = []

    # Dividir la cadena en bloques de 9 caracteres
    for i in range(0, len(cadena), 9):
        fila = [int(cadena[j]) for j in range(i, i + 9)]
        lista_9x9.append(fila)
    print(lista_9x9)
    return lista_9x9


